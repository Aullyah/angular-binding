export class Item{
	nama: string;
	nim: string;
	peminatan: string;
	nilai: number;
	grade: string;

	constructor(nama: string, nim: string, peminatan: string, nilai: number, grade: string){
		this.nama = nama;
		this.nim = nim;
		this.peminatan = peminatan;
		this.nilai = nilai;
		this.grade = grade;
	}

	

}